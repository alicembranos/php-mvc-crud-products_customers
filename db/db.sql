
DROP DATABASE IF EXISTS products;
CREATE DATABASE IF NOT EXISTS products;
USE products;

CREATE TABLE clientes (
   
   CÓDIGOCLIENTE      INT      NOT NULL   AUTO_INCREMENT,
   EMPRESA VARCHAR(50) NOT NULL,
   DIRECCIÓN VARCHAR(50) NOT NULL,
   POBLACIÓN VARCHAR(50) NOT NULL,
   TELÉFONO VARCHAR(50) NOT NULL,
   RESPONSABLE VARCHAR(50) NOT NULL,

   PRIMARY KEY (CÓDIGOCLIENTE)
);

CREATE TABLE productos (
   
   CÓDIGOARTÍCULO      INT      NOT NULL   AUTO_INCREMENT,
   SECCIÓN              VARCHAR(50) NOT NULL,
   NOMBREARTÍCULO VARCHAR(50) NOT NULL,
   PRECIO VARCHAR(50) NOT NULL,
   FECHA VARCHAR(50) NOT NULL,
   IMPORTADO VARCHAR(50) NOT NULL,
   PAÍSDEORIGEN VARCHAR(50) NOT NULL,

   PRIMARY KEY (CÓDIGOARTÍCULO)
);

CREATE TABLE pedidos (
   NUMERO_PEDIDO       INT             NOT NULL   AUTO_INCREMENT,
   CÓDIGOCLIENTE       INT      NOT NULL,
   CÓDIGOARTÍCULO      INT      NOT NULL,
  
   FOREIGN KEY (CÓDIGOCLIENTE )  REFERENCES clientes (CÓDIGOCLIENTE )    ON DELETE CASCADE,
   FOREIGN KEY (CÓDIGOARTÍCULO) REFERENCES productos (CÓDIGOARTÍCULO) ON DELETE CASCADE,
   PRIMARY KEY (NUMERO_PEDIDO)
);

